# Activating the Virtual Environment

# Now we need to activate the virtual environment using the following command:

# learning_log$ source ll_env/bin/activate
# (ll_env) learning_log$

# This command runs the script 'activate' in ll_env/bin. When the environment is active, you'll see the name of the environment in parentheses, as shown at line 6; then you can install packages to the environment and use packages that have already been installed. Packages you install in ll_env will be available only while the environment is active.

# NOTE - If you're using Windows, use the command ll_env\Scripts\activate (without the word source) to activea the virtual environment. If you're using Powershell, you might need to capitalize Activate.

# To stop using the virtual environment, enter 'deactivate'.

# The environment will also become inactive when you close the terminal it's running in.