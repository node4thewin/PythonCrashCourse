# 1. Print the message "The first three items in the list are:". Then use a slice to print the first three items from that program's list.
pizzas = ['pepperoni', 'pineapple', 'cheese', 'sausage', 'buffalo chicken']
print("The first three items in the list are:")
print(pizzas[0:3])

# 2. Print the message "Three items from the middle of the list are:". Use a slie to print three items from the middle of the list.
print("\nThree items from the middle of the list are:")
print(pizzas[1:4])

# Print the message "The last three items in the list are:". Use a slice to print three items from the middle of the list.
print("\nThe last three items in the list are:")
print(pizzas[:-4])