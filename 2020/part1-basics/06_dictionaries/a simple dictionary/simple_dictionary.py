# A SIMPLE DICTIONARY

# Consider a game featuring aliens that can have different colors and point values. This simple dictionary stores information about a particular alien:

alien_0 = {'color': 'green', 'points': 5}

print(alien_0['color'])
print(alien_0['points'])

# The dictionary 'alien_0' stores the alien's color and point value. The last two lines access and display that information, as shown here when executed.

# As with most new programming concepts, using dictionaries takes practice. Once you've worked with dictionaires for a bit you'll soon see how effectively they can model real-world situations.
