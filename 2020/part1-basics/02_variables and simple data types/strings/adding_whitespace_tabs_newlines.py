# In programming, whitespace refers to any nonprinting character, such as spaces tabs, end-of-line symbols. You can use whitespace to organize your output
# so it's easier for users to read.

# To add a tab to your text, use the character combination \t as shown below:

print('Python')

print('\tPython')

# To add a newline in a string, use the character combination \n:

print('Languarges:\nPython\nC\nJavaScript')

# You can also combine tabs and newlines in a single string. The string "\n\t" tells Python to move to a new line, and start the next line with a tab.
# The following example shows how you can use a one-line string to generate four lines of output:

print('Languages:\n\tPython\n\tC\n\tJavaScript')

