# Think of at least three different animals that have a common
# characteristic. Store the names of these animals in a list, and
# then use a for loop to print out the name of each animal.

pets = ['snake', 'dog', 'cat', 'gerbil', 'monkey', 'flying squirrel']
for pet in pets:
	print(f"Honestly, a {pet.title()} would make a great pet.")

print("All of these animals would make great pets! That's what they have in common!")

