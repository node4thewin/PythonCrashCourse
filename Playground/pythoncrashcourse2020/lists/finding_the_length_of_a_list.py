# FINDING THE LENGTH OF A LIST

# You can quickly find the length of a list by using the len() function. The list in this
# example has five items, so its length should be five. OPEN PYTHON3 for this example:

cars = ['jeep', 'toyota', 'ford', 'subaru', 'honda']
len(cars)

# The below example would be how to call it:

print(f'{len(cars)}')

