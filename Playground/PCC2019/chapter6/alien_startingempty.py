# STARTING WITH AN EMPTY DICTIONARY
# It is sometimes convenient or even necessary to start with an empty
# dictionary and then add each new item to it. Here is how to build a
# dictionary using this approach:

alien_0 = {}

alien_0['color'] = 'green'
alien_0['points'] = 5

print(alien_0)

