# MULTIPLE ASSIGNMENT
# You can assign values to more than one variable using just a single line. This can help
# shorten your programs and make them easier to read; you'll use this technique most often
# when initializing a set of numbers.

# For example, here's how you can initialize the variables x, y, and z to zero:

x, y, z, = 0, 0, 0,

print(x), print(y), print(z)
print(y)
print(z)

